//Max i min zna4enija
package p4;


public class p1 {
    public static void main(String[] args) {
        float num1 = 4.25f; //с плавающей запятой
        int num2 = 5;       //целое число
        System.out.println("Большее число из 2 чисел: " + Math.max(num1, num2)); //выводится double
        System.out.println("Меньшее число из 2 чисел: " + Math.min(num1, num2));  //выводится double
    }
}
